# Package là phương pháp quản lý các module theo package, 
# giúp việc xây dựng cấu trúc của ứng dụng Python trở nên đơn giản hơn rất nhiều.
# Package các gói dùng để chứa nhiều module liên quan với nhau
# package giống như folder trong máy tính vậy. Bên trong folder này có thể chứa thêm một folder khác (tức là package khác), 
# và cũng có thể chứa thêm những file khác (tức là các modules).

